#цикл for. перебор элементов из списка
words = ["hello", "world", "spam", "eggs"]
for word in words:
    print(word + "!")
#пример
letters = ['a', 'b','c']
for l in letters:
    print(l)
#выполнение цикла определенное количество раз
for i in range(5):
    print("hello!")
#пример
for i in range(0, 20, 2):
    print(i)