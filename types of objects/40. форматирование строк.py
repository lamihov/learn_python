#форматирование строк. Используется метод форматирвания format для замены аргументов строки
nums = [4, 5, 6]
msg = "Numbers: {0} {1} {2}".format(nums[0], nums[1], nums[2])
print(msg)

#пример
print("{0}{1}{0}".format("abra", "cad"))

#форматирование строк можно делать с помощью аргументов которым присвоены имена
a = "{x}, {y}".format(x = 5, y = 12)
print(a)

#пример
str = "{c}, {b}, {a}".format(a = 5, b = 9, c = 7)
print(str)